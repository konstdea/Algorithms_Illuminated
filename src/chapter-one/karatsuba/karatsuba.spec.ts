import { multiply } from './karatsuba';

describe('multiply', () => {
    it('12 * 56 = 672', () => {
        expect(multiply(12n, 56n)).toEqual(672n);
    });
    it('123 * 123 = 15129', () => {
        expect(multiply(123n, 123n)).toEqual(15129n);
    });
    it('1234 * 5678 = 7006652', () => {
        expect(multiply(1234n, 5678n)).toEqual(7006652n);
    });
    it('3141592653589793238462643383279502884197169399375105820974944592 * 2718281828459045235360287471352662497757247093699959574966967627', () => {
        // Output:8539734222673567065463550869546574495034888535765114961879601127067743044893204848617875072216249073013374895871952806582723184
        expect(multiply(
            3141592653589793238462643383279502884197169399375105820974944592n,
            2718281828459045235360287471352662497757247093699959574966967627n
        ).toString())
            .toEqual(8539734222673567065463550869546574495034888535765114961879601127067743044893204848617875072216249073013374895871952806582723184n.toString());

    });
});